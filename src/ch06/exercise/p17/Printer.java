package ch06.exercise.p17;

public class Printer {
    static void println(int p) {
        System.out.println(p);
    }

    static void println(boolean p) {
        System.out.println(p);
    }

    static void println(double p) {
        System.out.println(p);
    }

    static void println(String p) {
        System.out.println(p);
    }
}
